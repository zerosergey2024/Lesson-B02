# Шаг 1: Привести строку к нижнему регистру (если необходимо учитывать регистр)
# Шаг 2: Удалить все небуквенные символы (если необходимо учитывать только буквы)
# Шаг 3: Инициализировать две строки: одна для перебора вперед, другая для перебора назад
# Шаг 4: Перебрать символы строки вперед и добавить их в первую строку
# Шаг 5: Перебрать символы строки назад и добавить их во вторую строку
# Шаг 6: Сравнить две строки
# Шаг 7: Если строки равны, вернуть True, иначе вернуть False



def is_palindrome(s):
    # Шаг 1: Привести строку к нижнему регистру
    s = s.lower()

    # Шаг 2: Удалить все небуквенные символы
    s = ''.join(c for c in s if c.isalnum())

    # Шаг 3: Инициализировать две строки
    forward_str = ""
    backward_str = ""

    # Шаг 4: Перебрать символы строки вперед и добавить их в первую строку
    for char in s:
        forward_str += char

    # Шаг 5: Перебрать символы строки назад и добавить их во вторую строку
    for char in reversed(s):
        backward_str += char

    # Шаг 6: Сравнить две строки
    if forward_str == backward_str:
        # Шаг 7: Если строки равны, вернуть True
        return True
    else:
        # Шаг 7: Иначе вернуть False
        return False

# Пример использования функции
print(is_palindrome("A man, a plan, a canal, Panama"))  # True
print(is_palindrome("racecar"))  # True
print(is_palindrome("hello"))  # False